package com.cognitree.levelops.codebuild;

public class Calculator {

    //addition
    public int add(int x, int y) {
        return x + y;
    }

    //subtraction
    public int subtract(int x, int y) {
        return x - y;
    }

    //multiplication
    public int multiply(int x, int y) {
        return x * y;
    }    //addition

    //division
    public int divide(int x, int y) {
        return x / y;
    }
}
